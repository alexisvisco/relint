package lint017

import (
	"fmt"
	"go/ast"
	"strings"
	"unicode"

	"golang.org/x/tools/go/analysis"
	"golang.org/x/tools/go/analysis/passes/inspect"
	"golang.org/x/tools/go/ast/inspector"

	"github.com/alexisvisco/relint/analysisutil"
)

var Analyzer = &analysis.Analyzer{
	Name:     "lint017",
	Doc:      "LINT-017: Require* middleware in handler package must be in require_{name}.go",
	Run:      run,
	Requires: []*analysis.Analyzer{inspect.Analyzer},
}

func run(pass *analysis.Pass) (interface{}, error) {
	if pass.Pkg.Name() != "handler" {
		return nil, nil
	}

	insp := pass.ResultOf[inspect.Analyzer].(*inspector.Inspector)

	insp.Preorder([]ast.Node{(*ast.FuncDecl)(nil)}, func(n ast.Node) {
		fn := n.(*ast.FuncDecl)
		name := fn.Name.Name
		suffix, ok := extractSuffix(name, "Require", "require")
		if !ok || suffix == "" {
			return
		}
		expectedFile := fmt.Sprintf("require_%s.go", toSnake(suffix))
		actualFile := analysisutil.FileBasename(pass, fn.Name.Pos())
		if actualFile != expectedFile {
			pass.Reportf(fn.Name.Pos(), "LINT-017: middleware %q must be in file %q", name, expectedFile)
		}
	})

	return nil, nil
}

func extractSuffix(name string, prefixes ...string) (string, bool) {
	for _, prefix := range prefixes {
		if strings.HasPrefix(name, prefix) {
			return name[len(prefix):], true
		}
	}
	return "", false
}

func toSnake(s string) string {
	var result []rune
	for i, r := range s {
		if unicode.IsUpper(r) && i > 0 {
			result = append(result, '_')
		}
		result = append(result, unicode.ToLower(r))
	}
	return string(result)
}
